package gen

import (
	"bytes"
	"encoding/json"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"text/template"

	"github.com/polanski13/asyngo/spec"
)

func writeJSON(cfg *Config, doc *spec.AsyncAPI) error {
	data, err := json.MarshalIndent(doc, "", "  ")
	if err != nil {
		return fmt.Errorf("marshaling JSON: %w", err)
	}
	path := filepath.Join(cfg.OutputDir, "asyncapi.json")
	return os.WriteFile(path, data, 0o644)
}

func writeYAML(cfg *Config, doc *spec.AsyncAPI) error {
	jsonData, err := json.Marshal(doc)
	if err != nil {
		return fmt.Errorf("marshaling for YAML: %w", err)
	}
	yamlData, err := jsonToYAML(jsonData)
	if err != nil {
		return fmt.Errorf("converting to YAML: %w", err)
	}
	path := filepath.Join(cfg.OutputDir, "asyncapi.yaml")
	return os.WriteFile(path, yamlData, 0o644)
}

func writeGo(cfg *Config, doc *spec.AsyncAPI) error {
	path := filepath.Join(cfg.OutputDir, "docs.go")
	pkgName := filepath.Base(cfg.OutputDir)

	var buf bytes.Buffer
	tmpl := template.Must(template.New("docs").Parse(docsTemplate))
	err := tmpl.Execute(&buf, map[string]string{
		"PackageName": pkgName,
		"Title":       doc.Info.Title,
		"Version":     doc.Info.Version,
	})
	if err != nil {
		return fmt.Errorf("executing template: %w", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("formatting Go source: %w", err)
	}
	return os.WriteFile(path, formatted, 0o644)
}

const docsTemplate = `// Code generated by asyngo. DO NOT EDIT.

package {{.PackageName}}

import _ "embed"

//go:embed asyncapi.json
var DocJSON []byte

const Title = "{{.Title}}"

const Version = "{{.Version}}"
`
